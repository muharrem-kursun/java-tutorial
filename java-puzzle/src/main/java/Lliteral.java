public class Lliteral {
    public static void main(String[] args) {
        System.out.println(12345+54321);//sondaki sayı bir (1)
        System.out.println(12345+5432l);//sondaki küçük L burada bu 5432 sayısını  long olarak tanımlıyoruz.
        System.out.println(12345+5432L);//böyle yazarsak karışıklaıklara neden olmayız.
    }
}
